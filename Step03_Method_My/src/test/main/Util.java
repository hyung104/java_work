package test.main;		// 명칭 - 식별자명.  package 식별자명 test.main

public class Util {		// class 식별자명 Util
	// sing 이라는 이름의 static 멤버 메소드 정의하기
	public static void sing() {		// method 식별자명 sing
		System.out.println("노래를 불러요~");		// 클래스명. 아이디명 . method()
	}
	
	// dance라는 이름의  static 멤머 메소드 정의하기
	public static void dance() {
		System.out.println("춤을 춰요~");		
	}
}

package test.main;
/*
 * [ Data type ]
 *  
 * 2. 숫자형
 * 
 * - 정수형 : byte, short, int, long
 * - 실수형 : float, double
 */
public class MainClass02 {
	public static void main(String[] args) {
		//정수형 변수 선언과 동시에 값 대입하기
		byte iByte=10;
		short iShort=10;
		int iInt=10;
		long iLong=10;
		
		// 실수형 변수 선언과 도잇에 값 대입하기
		float num1=10.1f;	// 10.1은 에러남.
		double num2=10.2d;
		double num3=10.3;
	}
}
